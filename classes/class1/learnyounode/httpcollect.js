var http = require('http')
var bl = require('bl')

http.get(process.argv[2], function (response) {
  response.pipe(bl(function (err, data) {
    if (err)
      return console.error(err)
    data = data.toString()
    console.log(data.length)
    console.log(data)
  }))  
})

//bl runs the data collection function, and has last line callback(null, data)
//the rendering is in multiple lines because of newlines in the string.